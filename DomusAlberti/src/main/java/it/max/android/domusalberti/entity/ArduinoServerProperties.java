package it.max.android.domusalberti.entity;

public class ArduinoServerProperties {
    private String arduinoAddress = null;
    private String arduinoPort = null;

    public String getArduinoAddress() {
        return arduinoAddress;
    }

    public void setArduinoAddress(String arduinoAddress) {
        this.arduinoAddress = arduinoAddress;
    }

    public String getArduinoPort() {
        return arduinoPort;
    }

    public void setArduinoPort(String arduinoPort) {
        this.arduinoPort = arduinoPort;
    }
}
